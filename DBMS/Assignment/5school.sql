CREATE DATABASE SCHOOL_INFO;

USE SCHOOL_INFO;

CREATE TABLE TEACHER (
	TID INT PRIMARY KEY,
	TName VARCHAR(30),
	TAddress VARCHAR(10),
	TQualification VARCHAR(10)
);

CREATE TABLE SCHOOL (
	SID INT PRIMARY KEY,
	SName VARCHAR(30),
	SAddress VARCHAR(10),
	SPhone NUMERIC(10)
);

CREATE TABLE SCHOOL_TEACHER (
	SID INT,
	TID INT,
	No_of_Period INT(2)
);

