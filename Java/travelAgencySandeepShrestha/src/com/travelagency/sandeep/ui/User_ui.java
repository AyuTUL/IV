    private void viewPackageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPackageActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{"Package ID", "Name", "Duration", "Price"});
        for (PackageModel pkg : packageService.getAllPackages()) {
            model.addRow(new Object[]{pkg.getId(), pkg.getName(), pkg.getDuration(), pkg.getPrice()});
        }
    }//GEN-LAST:event_viewPackageActionPerformed

    private void bookPackageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookPackageActionPerformed
        String name = JOptionPane.showInputDialog(this, "Enter your name:");
        if (name == null) {
            return;
        }
        String phone = JOptionPane.showInputDialog(this, "Enter your phone:");
        if (phone == null) {
            return;
        }

        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Select a package first!");
            return;
        }
        int packageId = (int) jTable1.getValueAt(selectedRow, 0);
        bookingService.bookPackage(name, phone, packageId);
        JOptionPane.showMessageDialog(this, "Booking successful!");
    }//GEN-LAST:event_bookPackageActionPerformed

    private void cancelBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBookingActionPerformed
        String bookingIdStr = JOptionPane.showInputDialog(this, "Enter Booking ID to cancel:");
        if (bookingIdStr == null) {
            return;
        }
        try {
            int bookingId = Integer.parseInt(bookingIdStr);
            bookingService.cancelBooking(bookingId);
            JOptionPane.showMessageDialog(this, "Booking cancelled successfully!");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Booking ID");
        }
    }//GEN-LAST:event_cancelBookingActionPerformed

    private void viewBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBookingActionPerformed
        String phone = JOptionPane.showInputDialog(this, "Enter your phone to view bookings:");
        if (phone == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{"Booking ID", "Package Name", "Duration", "Price"});
        for (BookingModel booking : bookingService.getUserBookings(phone)) {
            model.addRow(new Object[]{booking.getBookingId(), booking.getPackageName(), booking.getDuration(), booking.getPrice()});
        }
    }//GEN-LAST:event_viewBookingActionPerformed

